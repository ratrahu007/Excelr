class ConcatandAppend{

	public static void main( String [] args )

	{

		String s1 =" Rahul";

		s1.concat("Rathod");

		System.out.println(s1);

		System.out.println("==========================================================");
	
		StringBuffer s2 = new StringBuffer("Khushal");

		s2.append("Patil");

		System.out.println(s2);

	}

}